// 구호 SONGDO에 대해 HIGHSCHOOL로 응원.
// 구호 CODE에 대해 MASTER로 응원.
// 구호 2023에 대해 0611로 응원.
// 구호 ALGORITHM에 대해 CONTEST로 응원.

#include <iostream>
using namespace std;

int main() {
    string s;
    cin >> s;
    if (s=="SONGDO") {
        cout << "HIGHSCHOOL" << endl;
    }
    else if (s=="CODE") {
        cout << "MASTER" << endl;
    }
    else if (s=="2023") {
        cout << "0611" << endl;
    }
    else if (s=="ALGOLITHM") {
        cout << "CONTEST" << endl;
    }
}